# Array Arithmetic
import numpy as np

a = np.array([10, 20, 30, 40])
b = np.array([1, 2, 3, 4])

print(f'a: {a}')
print(f'b: {b}\n')


print(f'a-2: {a-2}')

print(f'b+10: {b+10}')

print(f'a/5: {a/5}')

print(f'b^3: {b**3}\n')

print('Operands: ')
print(f'a: {a}')
print(f'b: {b}\n')

print(f'a+b: {a+b}')
print(f'a-b: {a-b}')

# Take the sin, cos, etc
print(f'Sin(b): {np.sin(b)}')
print(f'Cos(b): {np.cos(b)}')
